public class WhileLoop02 {
    public static void main(String[] args) {
		
        /*
         * while (condition){
         *   // you loop code here
         * }
         * 
         */
        // Print hello world 100 times 

        int counter = 0;

        while ( counter <= 100) {
            System.out.println("hello world");
            counter++;
        }


	}
}
